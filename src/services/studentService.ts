/**
 * Submit an assignment — tries RPC first (for magic-link students),
 * falls back to direct insert.
 */
export const submitAssignment = async (
  data: { assignment_id: string; student_id: string; file_url?: string; notes?: string }
): Promise<AssignmentSubmission> => {
  const session = getStudentSession();

  // Try RPC for magic-link students
  if (session?.access_token) {
    const { data: rpcData, error: rpcError } = await supabase
      .rpc('submit_assignment_by_token', {
        p_student_id: data.student_id,
        p_access_token: session.access_token,
        p_assignment_id: data.assignment_id,
        p_notes: data.notes || null,
        p_file_url: data.file_url || null,
      });

    if (!rpcError && rpcData) {
      // RPC returns a single row or array depending on Supabase version
      const submission = Array.isArray(rpcData) ? rpcData[0] : rpcData;
      return submission as AssignmentSubmission;
    }
    console.warn('RPC submit_assignment_by_token failed, falling back to direct insert:', rpcError?.message);
  }

  // Fallback: direct insert (works with anon INSERT policy)
  const { data: submission, error } = await supabase
    .from('assignment_submissions')
    .insert({
      assignment_id: data.assignment_id,
      student_id: data.student_id,
      file_url: data.file_url || null,
      notes: data.notes || null,
      status: 'submitted',
    })
    .select()
    .single();

  if (error) { console.error('Error submitting assignment:', error); throw error; }
  return submission as AssignmentSubmission;
};

