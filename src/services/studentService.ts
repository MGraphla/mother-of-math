/**
 * Get students who haven't submitted an assignment yet
 */
export const getUnsubmittedStudents = async (
  assignmentId: string,
  students: Student[]
): Promise<Student[]> => {
  const studentIds = students.map(s => s.id);
  
  // Get assigned students
  const { data: links } = await supabase
    .from('assignment_students')
    .select('student_id')
    .eq('assignment_id', assignmentId)
    .in('student_id', studentIds);

  const assignedIds = new Set((links || []).map((l: any) => l.student_id));

  // Get students who have submitted
  const { data: submissions } = await supabase
    .from('assignment_submissions')
    .select('student_id')
    .eq('assignment_id', assignmentId);

  const submittedIds = new Set((submissions || []).map((s: any) => s.student_id));

  // Return students who are assigned but haven't submitted
  return students.filter(s => assignedIds.has(s.id) && !submittedIds.has(s.id));
};

