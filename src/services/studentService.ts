/**
 * Get student submission statistics
 */
export interface StudentStats {
  totalAssigned: number;
  submitted: number;
  pending: number;
  graded: number;
  averageScore: number | null;
  latestSubmission: string | null;
}

export const getStudentStats = async (studentId: string): Promise<StudentStats> => {
  try {
    // Get assignments assigned to this student
    const { data: links } = await supabase
      .from('assignment_students')
      .select('assignment_id')
      .eq('student_id', studentId);

    const assignmentIds = (links || []).map((l: any) => l.assignment_id);

    // Get student's submissions
    const { data: submissions } = await supabase
      .from('assignment_submissions')
      .select('*')
      .eq('student_id', studentId)
      .order('submitted_at', { ascending: false });

    const allSubmissions = (submissions || []) as AssignmentSubmission[];
    const gradedOnes = allSubmissions.filter(s => 
      (s.status === 'graded' || s.status === 'returned') && s.score !== null
    );

    const avgScore = gradedOnes.length > 0
      ? gradedOnes.reduce((sum, s) => sum + (s.score || 0), 0) / gradedOnes.length
      : null;

    return {
      totalAssigned: assignmentIds.length,
      submitted: allSubmissions.length,
      pending: assignmentIds.length - allSubmissions.length,
      graded: gradedOnes.length,
      averageScore: avgScore ? Math.round(avgScore * 10) / 10 : null,
      latestSubmission: allSubmissions[0]?.submitted_at || null,
    };
  } catch (err) {
    console.error('Error getting student stats:', err);
    return {
      totalAssigned: 0,
      submitted: 0,
      pending: 0,
      graded: 0,
      averageScore: null,
      latestSubmission: null,
    };
  }
};

